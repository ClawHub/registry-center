package com.clawhub.registrycenter.client;

/**
 * The type Client bean.
 */
public class ClientBean {
    /**
     * The Role.
     */
    private String role;
    /**
     * The Ip.
     */
    private String ip;
    /**
     * The Port.
     */
    private String port;
    /**
     * The Server.
     */
    private String server;
    /**
     * The Active.
     */
    private String active;

    /**
     * Gets role.
     *
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * Sets role.
     *
     * @param role the role
     */
    public void setRole(String role) {
        this.role = role;
    }

    /**
     * Gets ip.
     *
     * @return the ip
     */
    public String getIp() {
        return ip;
    }

    /**
     * Sets ip.
     *
     * @param ip the ip
     */
    public void setIp(String ip) {
        this.ip = ip;
    }

    /**
     * Gets port.
     *
     * @return the port
     */
    public String getPort() {
        return port;
    }

    /**
     * Sets port.
     *
     * @param port the port
     */
    public void setPort(String port) {
        this.port = port;
    }

    /**
     * Gets service.
     *
     * @return the service
     */
    public String getServer() {
        return server;
    }

    /**
     * Sets service.
     *
     * @param server the service
     */
    public void setServer(String server) {
        this.server = server;
    }

    /**
     * Gets active.
     *
     * @return the active
     */
    public String getActive() {
        return active;
    }

    /**
     * Sets active.
     *
     * @param active the active
     */
    public void setActive(String active) {
        this.active = active;
    }
}
